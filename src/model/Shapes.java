package model;

import java.awt.Color;
import java.awt.Shape;

/**
 * A class that contains a collections of shapes.
 * @author Anh Nguyen
 * @version 1.0
 */
public class Shapes {
    
    //Class instance field
    /**
     * The current shape that is being drawn.
     */
    private final Shape myCurrentShape;
    
    /**
     * myCurrentColor is the current color selected.
     */
    private final Color myCurrentColor;
    
    /**
     * myCurrentStroke contains a stroke value for the thickness of the shape.
     */
    private final int myCurrentStroke;
    
    /**
     * The constructor for Shapes.
     * @param theShape the type of shape is being drawn.
     * @param theColor the color of the shape.
     * @param theStroke the thickness of the shape.
     */
    public Shapes(final Shape theShape, final Color theColor, final int theStroke) {
        
        myCurrentShape = theShape;
        myCurrentColor = theColor;
        myCurrentStroke = theStroke;
        
    }
    /**
     * A method that returns the shape that is being drawn.
     * @return myCurrentShape the current shape.
     */
    public Shape getShape() {
        return myCurrentShape;   
    }
    
    /**
     * A method to get the current color.
     * @return myCurrentColor the current color selected.
     */
    public Color getColor() {
        return myCurrentColor;
    }
    
    /**
     * A method to get the width of the stroke.
     * @return myCurrentStroke an int value of the thickness of the shapes.
     */
    public int getStroke() {
        return myCurrentStroke;
    }
    
    
    

}
